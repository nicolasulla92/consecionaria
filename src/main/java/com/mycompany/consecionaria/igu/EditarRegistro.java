    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(txtMarca.getText().equals("")||txtModelo.getText().equals("")||
                txtMotor.getText().equals("")||txtColor.getText().equals("")||
                txtPatente.getText().equals("")){
                mostrarMensaje("Todos los campos deben ser completados", "Error", "Campos vacios");
        }else{
            ctrlLogica.guardarEdicion(idEditar, txtMarca.getText(), txtModelo.getText(), txtMotor.getText(), txtColor.getText(), txtPatente.getText());
            mostrarMensaje("El registro ha sido editado correctamente", "Info", "Editado correctamente");
            this.dispose();
            VisualizacionRED pantalla = new VisualizacionRED();
            pantalla.setVisible(true);
            pantalla.setLocationRelativeTo(null);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


